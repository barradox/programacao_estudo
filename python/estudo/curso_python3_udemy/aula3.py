"""
Python = linguagem de programação
Tipo de tipagem = dinâmica/forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Vinicius Moraes')

# Aspas duplas
print("Vinicius Moraes")

# Escape (\ faz o interpretador pular o próximo caractere)
print("Vinicius \"de\" Moraes")

# r
print(r"Vinicius \"de\" Moraes")
