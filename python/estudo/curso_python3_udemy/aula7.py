# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar números e underline _
# O sinal de = é o operador de atribuição. Ele é utilizado para atribuir um valor
# a uma variável.
# Uso: nome_variavel = expressao
nome_completo = 'Vinicius Moraes Pereira'
print(nome_completo)

nome = 'Vinicius'
idade = 30
maior_de_idade = idade >= 18
print('Nome:', nome, 'Idade:', idade)
print('É maior?', maior_de_idade)